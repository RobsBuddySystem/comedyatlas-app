/* Floating "Feedback" widget for COMEDY ATLAS pages. Self-contained: inject a
   pill button that opens a small form and POSTs to the ATLAS API /feedback
   endpoint. Works from either origin (pariscomedy.com static pages and the
   atlas-api-served portal pages) — the API's CORS allows pariscomedy.com and
   the endpoint is public (a logged-in visitor is auto-attributed server-side
   via their session cookie; credentials:'include' carries it). No dependency.

   This file is also the ONE script guaranteed to load on every COMEDY ATLAS
   page, hand-authored (index.html, city.html, itinerary.html, ...) AND
   generated (scripts/generate_entity_pages.py / generate_city_pages.py /
   generate_blog_pages.py / generate_cities_index.py, via seo_common.py's
   FEEDBACK_SCRIPT_HTML, which every generator's footer_html() emits). Two
   features that must work identically on both page families without
   touching any generator therefore live in this same file:
     1. the mobile hamburger nav disclosure (roadmap #16) — see
        initHamburgerNav() below;
     2. venue-correction context on the feedback modal (roadmap #17) — see
        the venueSlug/quickPicks handling in AtlasFeedback.open() below. */
(function () {
  "use strict";

  // -------------------------------------------------------------------
  // Roadmap #16 — hamburger mobile nav. Pure CSS + a few lines of JS, no
  // markup changes required anywhere: every COMEDY ATLAS page (hand-
  // authored or generated by seo_common.nav_html()) emits the SAME
  // <nav><a class="nav-logo">...</a><div class="nav-links">...</div></nav>
  // shape (verified: scripts/seo_common.py's PARISCOMEDY_NAV/ATLAS_APP_NAV
  // and every hand-authored page's <nav> block use identical class names).
  // That means a toggle button can be injected generically, by class
  // selector alone, from this one shared script — no per-page markup, no
  // generator edit. Simple disclosure, not a full menu system: no focus
  // trap, Escape closes it, a click outside closes it, aria-expanded is
  // kept in sync for screen readers.
  // -------------------------------------------------------------------
  function initHamburgerNav() {
    var navEls = document.querySelectorAll("nav");
    if (!navEls.length) return;

    var style = document.createElement("style");
    // NOTE ON ORDER: the un-media-queried base rule (hidden by default)
    // must come BEFORE the @media(max-width:480px) override in source
    // order -- two rules of equal specificity resolve by source order
    // regardless of which @media blocks currently match, so if the base
    // rule were written second it would win at EVERY width, including
    // narrow ones, and the toggle would never become visible.
    style.textContent =
      ".atlas-nav-toggle{display:none;align-items:center;justify-content:center;" +
      "width:36px;height:36px;border:1px solid var(--border,#1e2a3a);border-radius:8px;" +
      "background:transparent;color:var(--text,inherit);cursor:pointer;padding:0;" +
      "font-size:18px;line-height:1;margin-left:auto}" +
      ".atlas-nav-toggle:hover{border-color:var(--purple,#7c3aed)}" +
      "@media(max-width:480px){" +
      ".atlas-nav-toggle{display:inline-flex}" +
      "nav .nav-links{display:none;flex-direction:column;align-items:flex-start;" +
      "position:absolute;left:0;right:0;top:100%;background:var(--bg,#0a0e1a);" +
      "border-bottom:1px solid var(--border,#1e2a3a);padding:10px 20px 14px;gap:12px;" +
      "z-index:99}" +
      "nav .nav-links.atlas-nav-open{display:flex}" +
      "}";
    document.head.appendChild(style);

    navEls.forEach(function (nav, i) {
      var links = nav.querySelector(".nav-links");
      if (!links) return; // no-op on any <nav> that doesn't match this site's shape
      if (!links.id) links.id = "atlas-nav-links-" + i;

      var btn = document.createElement("button");
      btn.type = "button";
      btn.className = "atlas-nav-toggle";
      btn.setAttribute("aria-label", "Menu");
      btn.setAttribute("aria-expanded", "false");
      btn.setAttribute("aria-controls", links.id);
      btn.innerHTML = "&#9776;"; // ☰
      nav.appendChild(btn);

      function setOpen(open) {
        links.classList.toggle("atlas-nav-open", open);
        btn.setAttribute("aria-expanded", open ? "true" : "false");
        btn.innerHTML = open ? "&#10005;" : "&#9776;"; // ✕ / ☰
      }

      btn.addEventListener("click", function (e) {
        e.stopPropagation();
        setOpen(!links.classList.contains("atlas-nav-open"));
      });
      document.addEventListener("click", function (e) {
        if (links.classList.contains("atlas-nav-open") &&
            !nav.contains(e.target)) setOpen(false);
      });
      document.addEventListener("keydown", function (e) {
        if (e.key === "Escape" && links.classList.contains("atlas-nav-open")) {
          setOpen(false);
          btn.focus();
        }
      });
      // Picking a nav link closes the disclosure instead of leaving it
      // open behind the page the link just navigated to.
      links.querySelectorAll("a").forEach(function (a) {
        a.addEventListener("click", function () { setOpen(false); });
      });
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", initHamburgerNav);
  } else {
    initHamburgerNav();
  }
})();

(function () {
  "use strict";
  var API_BASE = (function (h) {
    if (h === "atlas-api.pariscomedy.com" || h === "api.comedyatlas.app") return "";
    if (h === "comedyatlas.app" || h === "www.comedyatlas.app") return "https://api.comedyatlas.app";
    return "https://atlas-api.pariscomedy.com";
  })(location.hostname);

  var css = "" +
    // 2026-07-19 QA fix (bug #8, "floating FAB overlaps content at 390px"):
    // bottom offset now reserves env(safe-area-inset-bottom) (iOS home-
    // indicator / gesture bar), so on a notched/gesture phone the pill sits
    // clear of the OS chrome instead of nearly touching it. Confirmed live
    // at 390x844 pre-fix: the button's own bounding box (bottom ~832px in
    // an 844px-tall viewport) left only ~12px clearance and its center
    // point hit-tested to a real content node (a "You might also like"
    // section heading) sitting underneath it.
    ".atlas-fb-btn{position:fixed;right:16px;bottom:calc(16px + env(safe-area-inset-bottom,0px));z-index:9998;background:#7c3aed;color:#fff;border:none;border-radius:999px;padding:10px 16px;font:600 13px -apple-system,BlinkMacSystemFont,'Segoe UI',sans-serif;cursor:pointer;box-shadow:0 4px 14px rgba(0,0,0,.3)}" +
    ".atlas-fb-btn:hover{opacity:.92}" +
    // 2026-07-17 frontend audit: on narrow viewports this fixed pill had
    // no scroll-safe reservation and sat directly on top of whatever card
    // was in the bottom-right corner as the user scrolled (confirmed
    // obscuring real card text). Shrinking the footprint on mobile
    // directly reduces how much it can cover, without losing the
    // always-reachable fixed-position purpose the widget exists for.
    // 2026-07-19: further shrunk to an icon-only circular button below
    // 480px (was still a full "💬 Feedback" pill, wider than needed) and
    // anchored with the same safe-area-aware bottom offset.
    "@media(max-width:480px){.atlas-fb-btn{right:12px;bottom:calc(12px + env(safe-area-inset-bottom,0px));width:44px;height:44px;padding:0;font-size:18px;line-height:44px;text-align:center;border-radius:50%}.atlas-fb-btn-label{display:none}}" +
    ".atlas-fb-modal{position:fixed;inset:0;z-index:9999;background:rgba(0,0,0,.55);display:none;align-items:center;justify-content:center}" +
    ".atlas-fb-card{background:#111827;color:#f0f0f0;border:1px solid #1e2a3a;border-radius:12px;max-width:420px;width:calc(100% - 32px);padding:20px;font:14px -apple-system,BlinkMacSystemFont,'Segoe UI',sans-serif}" +
    "@media(prefers-color-scheme:light){.atlas-fb-card{background:#fff;color:#171512;border-color:#e2ddd2}}" +
    ".atlas-fb-card h3{margin:0 0 4px;font-size:16px}.atlas-fb-card p{margin:0 0 12px;color:#8899aa;font-size:12.5px}" +
    ".atlas-fb-card textarea,.atlas-fb-card input{width:100%;box-sizing:border-box;padding:9px 11px;border-radius:8px;border:1px solid #1e2a3a;background:transparent;color:inherit;font:inherit;margin-bottom:10px}" +
    ".atlas-fb-card textarea{min-height:90px;resize:vertical}" +
    ".atlas-fb-row{display:flex;gap:8px;justify-content:flex-end}" +
    ".atlas-fb-row button{padding:9px 16px;border-radius:8px;border:none;font-weight:700;cursor:pointer}" +
    ".atlas-fb-send{background:#7c3aed;color:#fff}.atlas-fb-cancel{background:transparent;border:1px solid #8899aa;color:#8899aa}" +
    ".atlas-fb-msg{font-size:12.5px;margin-top:6px}" +
    // Roadmap #17: venue-correction quick-picks. Hidden by default; shown
    // only when the modal opens with a venueSlug (venue page, or an event
    // page whose venue block supplies one — see detectVenueContext()).
    ".atlas-fb-quickpicks{display:none;flex-wrap:wrap;gap:6px;margin-bottom:10px}" +
    ".atlas-fb-qp{font-size:12px;font-weight:700;padding:6px 11px;border-radius:999px;" +
    "border:1px solid #8899aa;background:transparent;color:inherit;cursor:pointer}" +
    ".atlas-fb-qp.active{border-color:#7c3aed;background:rgba(124,58,237,.15);color:#9d5ff5}";

  function el(html) { var d = document.createElement("div"); d.innerHTML = html; return d.firstElementChild; }

  // Per-subject copy for the "Is something missing?" footer CTA (every page,
  // hand-authored + generated, see FOOTER in scripts/seo_common.py) vs. the
  // generic floating pill. The endpoint itself has no separate "category"
  // column -- FeedbackBody only has body/subject/email/page_ref (see
  // apps/atlas_api/main.py's /feedback) -- so "category" is carried in the
  // existing `subject` field rather than inventing a new one server-side.
  var SUBJECT_COPY = {
    suggestion: {
      heading: "Tell us what's missing",
      body: "Missing show, venue, comic, or city? Tell us and we'll add it.",
      placeholder: "What's missing? Include the show/venue/comic name and city if you can."
    },
    // ROADMAP_GAPS_2026-07-19.md item 11 (accessibility info fields):
    // discreet CTA on venue pages so real coverage can grow via the same
    // human-triage loop (scripts/triage/feedback_triage.py) every other
    // feedback subject already goes through -- never a guess, always a
    // real report from someone who's actually been there.
    venue_accessibility: {
      heading: "Know this venue's accessibility?",
      body: "Step-free access, accessible toilets, hearing loop -- anything you know helps other fans.",
      placeholder: "What should we know about getting in and around this venue?"
    }
  };

  // Roadmap #17 — venue-correction quick-picks. Each entry's `tag` is the
  // short marker prefixed onto the sent body (the /feedback endpoint has no
  // separate structured-category column -- see apps/atlas_api/main.py's
  // FeedbackBody -- so, same convention as `subject` above, the category is
  // carried as plain text inside the fields that already exist) and
  // `starter` is placeholder/prefill text that nudges the visitor toward a
  // useful, specific report instead of a bare "wrong".
  var VENUE_QUICK_PICKS = [
    { key: "wrong_address", label: "Wrong address", starter: "The address is wrong — it should be: " },
    { key: "closed", label: "Closed", starter: "This venue has closed. " },
    { key: "wrong_name", label: "Wrong name", starter: "The venue name is wrong — it should be: " }
  ];

  var pendingOpts = null;  // set by AtlasFeedback.open(opts) before the modal exists yet
  var openModalFn = null;  // bound once init() has built the DOM

  // Matches this site's own venue-page URL shape (/comedy-atlas/venue/<slug>/,
  // on either origin -- see scripts/generate_entity_pages.py's etype="venue"
  // pages). Also used, as a fallback, to pick up a venue's slug out of any
  // `/comedy-atlas/venue/<slug>/` link or `[data-venue-slug]` element on the
  // CURRENT page (e.g. a future event-page venue block) without requiring a
  // dedicated generator change -- forward-compatible, not made in this pass.
  var VENUE_PATH_RE = /\/comedy-atlas\/venue\/([^/]+)\/?/;

  function detectVenueSlug() {
    var onPage = location.pathname.match(VENUE_PATH_RE);
    if (onPage) return decodeURIComponent(onPage[1]);
    var explicit = document.querySelector("[data-venue-slug]");
    if (explicit) return explicit.getAttribute("data-venue-slug");
    var link = document.querySelector('a[href*="/comedy-atlas/venue/"]');
    if (link) {
      var m = (link.getAttribute("href") || "").match(VENUE_PATH_RE);
      if (m) return decodeURIComponent(m[1]);
    }
    return null;
  }

  function init() {
    var style = document.createElement("style"); style.textContent = css; document.head.appendChild(style);
    var btn = el('<button class="atlas-fb-btn" aria-label="Send feedback">💬<span class="atlas-fb-btn-label"> Feedback</span></button>');
    var modal = el('<div class="atlas-fb-modal"><div class="atlas-fb-card">' +
      '<h3 id="atlas-fb-heading">Help us improve ATLAS</h3>' +
      '<p id="atlas-fb-copy">Found a bug, missing show, or have an idea? Tell us — we read every note.</p>' +
      '<div class="atlas-fb-quickpicks" id="atlas-fb-quickpicks"></div>' +
      '<textarea id="atlas-fb-body" placeholder="What should we fix or add?"></textarea>' +
      '<input id="atlas-fb-email" type="email" placeholder="Your email (optional, so we can reply)">' +
      '<div class="atlas-fb-row"><button class="atlas-fb-cancel">Cancel</button>' +
      '<button class="atlas-fb-send">Send</button></div>' +
      '<div class="atlas-fb-msg" id="atlas-fb-msg"></div></div></div>');
    document.body.appendChild(btn); document.body.appendChild(modal);

    var currentSubject = null;
    var currentQuickPick = null; // selected VENUE_QUICK_PICKS[i].key, or null
    var qpContainer = document.getElementById("atlas-fb-quickpicks");

    function renderQuickPicks(picks) {
      currentQuickPick = null;
      qpContainer.innerHTML = "";
      if (!picks || !picks.length) { qpContainer.style.display = "none"; return; }
      qpContainer.style.display = "flex";
      picks.forEach(function (p) {
        var chip = document.createElement("button");
        chip.type = "button";
        chip.className = "atlas-fb-qp";
        chip.textContent = p.label;
        chip.addEventListener("click", function () {
          var body = document.getElementById("atlas-fb-body");
          var wasActive = chip.classList.contains("active");
          Array.prototype.forEach.call(qpContainer.children, function (c) { c.classList.remove("active"); });
          if (wasActive) {
            currentQuickPick = null;
          } else {
            chip.classList.add("active");
            currentQuickPick = p.key;
            // Only pre-fill an EMPTY box -- never clobber text the visitor
            // already started typing (e.g. re-picking a different chip).
            if (!body.value.trim()) body.value = p.starter;
          }
          body.focus();
        });
        qpContainer.appendChild(chip);
      });
    }

    // opts: { subject, venueSlug, quickPicks } -- venueSlug/quickPicks are
    // new (roadmap #17); a bare subject string (or null) is still accepted
    // for every existing caller (footer CTAs, the plain floating-pill click).
    function open(opts) {
      if (typeof opts === "string" || opts == null) opts = { subject: opts || null };
      var venueSlug = opts.venueSlug || null;
      currentSubject = venueSlug ? ("venue-correction: " + venueSlug) : (opts.subject || null);

      if (venueSlug) {
        document.getElementById("atlas-fb-heading").textContent = "Report an issue with this venue";
        document.getElementById("atlas-fb-copy").textContent =
          "Wrong address, closed down, or a typo in the name? Pick what's wrong (or just describe it) and we'll fix it.";
        document.getElementById("atlas-fb-body").placeholder = "What's wrong with this venue's listing?";
        renderQuickPicks(opts.quickPicks || VENUE_QUICK_PICKS);
      } else {
        var copy = (currentSubject && SUBJECT_COPY[currentSubject]) || null;
        document.getElementById("atlas-fb-heading").textContent = copy ? copy.heading : "Help us improve ATLAS";
        document.getElementById("atlas-fb-copy").textContent = copy ? copy.body :
          "Found a bug, missing show, or have an idea? Tell us — we read every note.";
        document.getElementById("atlas-fb-body").placeholder = copy ? copy.placeholder : "What should we fix or add?";
        renderQuickPicks(null);
      }
      modal.style.display = "flex";
      document.getElementById("atlas-fb-body").focus();
    }
    function close() { modal.style.display = "none"; }
    btn.addEventListener("click", function () {
      var venueSlug = detectVenueSlug();
      open(venueSlug ? { venueSlug: venueSlug } : null);
    });
    modal.addEventListener("click", function (e) { if (e.target === modal) close(); });
    modal.querySelector(".atlas-fb-cancel").addEventListener("click", close);
    modal.querySelector(".atlas-fb-send").addEventListener("click", function () {
      var body = document.getElementById("atlas-fb-body").value.trim();
      var email = document.getElementById("atlas-fb-email").value.trim();
      var msg = document.getElementById("atlas-fb-msg");
      if (body.length < 3) { msg.textContent = "Please add a message."; msg.style.color = "#c41e3a"; return; }
      if (currentQuickPick) body = "[" + currentQuickPick + "] " + body;
      msg.textContent = "Sending…"; msg.style.color = "#8899aa";
      fetch(API_BASE + "/feedback", {
        method: "POST", credentials: "include",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          body: body, email: email || null, subject: currentSubject,
          page_ref: location.pathname + location.search
        })
      }).then(function (r) { return r.json(); }).then(function (d) {
        msg.textContent = d.message || "Thank you!"; msg.style.color = "#3fb950";
        document.getElementById("atlas-fb-body").value = "";
        setTimeout(close, 1600);
      }).catch(function () { msg.textContent = "Could not send — try again."; msg.style.color = "#c41e3a"; });
    });

    openModalFn = open;
    if (pendingOpts !== undefined && pendingOpts !== null) { open(pendingOpts); pendingOpts = null; }
  }

  // Public API so any page's footer CTA (or anything else) can open this
  // same widget/backend instead of building a second feedback form. Safe to
  // call before DOMContentLoaded -- the requested opts are queued and
  // opened as soon as init() builds the modal.
  window.AtlasFeedback = {
    open: function (opts) {
      if (openModalFn) { openModalFn(opts); } else { pendingOpts = opts; }
    },
    // Convenience wrapper for any page (venue, event, or otherwise) that
    // wants to trigger venue-correction context explicitly, e.g. a future
    // "Report an issue with this venue" link inside an event page's venue
    // block: window.AtlasFeedback.openVenueCorrection(slug).
    openVenueCorrection: function (venueSlug, quickPickKey) {
      var opts = { venueSlug: venueSlug };
      if (quickPickKey) opts.quickPicks = VENUE_QUICK_PICKS.filter(function (p) { return p.key === quickPickKey; });
      this.open(opts);
    }
  };

  if (document.readyState === "loading") document.addEventListener("DOMContentLoaded", init);
  else init();
})();
